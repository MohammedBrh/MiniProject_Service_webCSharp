package Model;

/**
 *
 * @author Mohammed
 */
public class User {
    private String email;
    private String nom;
    private int id;
    private String isAdmin;
    private String cne;

    public User() {
    }

    public User(String email, String nom, String isAdmin, String cne) {
        this.email = email;
        this.nom = nom;
        this.isAdmin = isAdmin;
        this.cne = cne;
    }

    public String getIsAdmin() {
        return isAdmin;
    }

    public void setIsAdmin(String isAdmin) {
        this.isAdmin = isAdmin;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCne() {
        return cne;
    }

    public void setCne(String cne) {
        this.cne = cne;
    }
    
}
